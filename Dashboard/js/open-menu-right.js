document.getElementById("cont1").addEventListener('mouseover', open1);

function open1() {
    document.getElementById("cont1").style.transform = 'translateX(-30vh)';
    document.getElementById("img1").style.transform = 'scale(2, 2)';
    document.getElementById("img1").style.marginLeft = '2vw';
    document.getElementById("cont1").classList.add("openMenuRight");
    document.getElementById("img1").classList.add("appearMenuRight");
    document.getElementById("txt1").classList.add("appearMenuRight");
}

