document.getElementById("cont1").addEventListener('mouseout', close1);

function close1() {
    document.getElementById("cont1").style.transform = 'translateX(-0vh)';
    document.getElementById("img1").style.transform = 'scale(1, 1)';
    document.getElementById("img1").style.marginLeft = '0.25vw';
    document.getElementById("cont1").classList.remove("openMenuRight");
    document.getElementById("img1").classList.remove("appearMenuRight");
    document.getElementById("txt1").classList.remove("appearMenuRight");
}

